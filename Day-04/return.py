# by using return
def addition( num1 , num2):
    add = num1 + num2
    return add
print(addition(5 , 10))
def sub(num1 , num2):
    s = num1 - num2
    return s
print(sub(31 , 10))
def mul(num1 , num2):
    m = num1 * num2
    return m
print(mul(3 , 10))

def div(num1 , num2):
    d = num1 / num2
    return d
print (div(12, 3))

