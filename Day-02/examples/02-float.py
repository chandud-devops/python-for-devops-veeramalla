# Flot variables
num1 = 5.123456
num2 = 2.5
# Basic Arithmetic

result1 = num1 + num2
print("Addition:", result1)


result2 = num1 - num2
print("Substration:", result2)

result3 = num1 * num2
print("Multiplication:", result3)

result4 = num1 / num2
print("Division:", result4)

# Rounding
result5 = round(result1, 2)
result6 = round(result2, 3)
# Rounds to 2 decimal places
print("Rounded:", result5)
print("Round of sub:", result6)


